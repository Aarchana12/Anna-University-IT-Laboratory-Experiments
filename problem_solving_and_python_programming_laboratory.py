import math

def compute_sin_series(x, n_terms):
    # Compute sin(x) using Taylor series
    result = 0
    for i in range(n_terms):
        term = ((-1) ** i) * (x ** (2 * i + 1)) / math.factorial(2 * i + 1)
        result += term
    return result

