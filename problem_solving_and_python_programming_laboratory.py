import pygame
import sys

# Initialize Pygame
pygame.init()

# Set up the display
width, height = 800, 600
screen = pygame.display.set_mode((width, height))
pygame.display.set_caption("Bouncing Ball Game")

# Set up colors
black = (0, 0, 0)
white = (255, 255, 255)

# Set up the ball
ball_radius = 20
ball_x = width // 2
ball_y = height // 2
ball_speed_x = 5
ball_speed_y = 5

# Set up the paddle
paddle_width = 100
paddle_height = 20
paddle_x = (width - paddle_width) // 2
paddle_y = height - paddle_height - 10
paddle_speed = 10

# Main game loop
while True:
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            pygame.quit()
            sys.exit()

    # Move the paddle based on the keys
    keys = pygame.key.get_pressed()
    if keys[pygame.K_LEFT] and paddle_x > 0:
        paddle_x -= paddle_speed
    if keys[pygame.K_RIGHT] and paddle_x < width - paddle_width:
        paddle_x += paddle_speed

    # Move the ball
    ball_x += ball_speed_x
    ball_y += ball_speed_y

    # Bounce the ball off the walls
    if ball_x <= 0 or ball_x >= width - ball_radius * 2:
        ball_speed_x = -ball_speed_x
    if ball_y <= 0:
        ball_speed_y = -ball_speed_y

    # Bounce the ball off the paddle
    if paddle_x <= ball_x <= paddle_x + paddle_width and paddle_y <= ball_y <= paddle_y + paddle_height:
        ball_speed_y = -ball_speed_y

    # Check if the ball fell off the bottom of the screen
    if ball_y >= height:
        # Reset the ball position
        ball_x = width // 2
        ball_y = height // 2

    # Update the display
    screen.fill(black)
    pygame.draw.circle(screen, white, (ball_x, ball_y), ball_radius)
    pygame.draw.rect(screen, white, (paddle_x, paddle_y, paddle_width, paddle_height))

    pygame.display.flip()

    # Cap the frame rate
    pygame.time.Clock().tick(60)