# Generate Pascal's Triangle
def generate_pascals_triangle(rows):
    triangle = [[1] * (i + 1) for i in range(rows)]

    for i in range(2, rows):
        for j in range(1, i):
            triangle[i][j] = triangle[i - 1][j - 1] + triangle[i - 1][j]

    return triangle

