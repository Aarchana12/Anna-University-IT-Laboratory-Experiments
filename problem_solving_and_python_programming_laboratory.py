def calculate_steel_bar_weight(length, diameter):
    # Assuming a simple density for steel
    steel_density = 7.85  # in g/cm^3
    volume = math.pi * (diameter / 2) ** 2 * length
    weight = volume * steel_density / 1000  # Convert g to kg
    return weight

